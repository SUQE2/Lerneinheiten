create or replace function public.decide_group_join_request(
  target_group_id uuid,
  target_user_id uuid,
  approve_request boolean
)
returns void
language plpgsql
security definer
set search_path = ''
as $$
declare
  current_size integer;
  maximum_size integer;
begin
  if not public.is_group_admin(target_group_id) then
    raise exception 'Nur Admins dürfen Beitrittsanfragen bearbeiten.';
  end if;
  if not exists (
    select 1 from public.group_join_requests
    where group_id = target_group_id and user_id = target_user_id and status = 'pending'
  ) then
    raise exception 'Diese Beitrittsanfrage ist nicht mehr offen.';
  end if;

  if coalesce(approve_request, false) then
    select max_members into maximum_size from public.groups where id = target_group_id for update;
    select count(*) into current_size from public.group_members where group_id = target_group_id;
    if current_size >= maximum_size then
      raise exception 'Diese Gruppe hat ihre maximale Mitgliederzahl erreicht.';
    end if;
    insert into public.group_members (group_id, user_id, role)
    values (target_group_id, target_user_id, 'member')
    on conflict (group_id, user_id) do nothing;
  end if;

  update public.group_join_requests
  set status = case when coalesce(approve_request, false) then 'approved' else 'rejected' end,
      decided_at = now(), decided_by = (select auth.uid())
  where group_id = target_group_id and user_id = target_user_id;
  perform public.write_group_audit(
    target_group_id,
    case when coalesce(approve_request, false) then 'join_approved' else 'join_rejected' end,
    target_user_id
  );
end;
$$;


create or replace function public.transfer_group_ownership(target_group_id uuid, target_user_id uuid)
returns void
language plpgsql
security definer
set search_path = ''
as $$
begin
  if not exists (
    select 1 from public.group_members
    where group_id = target_group_id and user_id = (select auth.uid()) and role = 'owner'
  ) then
    raise exception 'Nur der Hauptadmin darf den Besitz übertragen.';
  end if;
  if target_user_id = (select auth.uid()) or not exists (
    select 1 from public.group_members where group_id = target_group_id and user_id = target_user_id
  ) then
    raise exception 'Wähle ein anderes Mitglied dieser Gruppe.';
  end if;
  update public.group_members set role = 'admin'
  where group_id = target_group_id and user_id = (select auth.uid());
  update public.group_members set role = 'owner'
  where group_id = target_group_id and user_id = target_user_id;
  update public.groups set owner_id = target_user_id where id = target_group_id;
  perform public.write_group_audit(target_group_id, 'ownership_transferred', target_user_id);
end;
$$;


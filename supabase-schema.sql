create or replace function public.write_group_audit(
  target_group_id uuid,
  action_name text,
  target_user_id uuid default null,
  action_details jsonb default '{}'::jsonb
)
returns void
language sql
security definer
set search_path = ''
as $$
  insert into public.group_audit_log (group_id, actor_id, action, target_user_id, details)
  values (
    target_group_id,
    (select auth.uid()),
    action_name,
    target_user_id,
    coalesce(action_details, '{}'::jsonb)
    || jsonb_build_object(
      'actorName', coalesce((select display_name from public.profiles where id = (select auth.uid())), 'Unbekannt')
    )
    || case
      when target_user_id is null then '{}'::jsonb
      else jsonb_build_object(
        'targetName', coalesce((select display_name from public.profiles where id = target_user_id), 'Unbekannt')
      )
    end
  );
$$;

create or replace function public.create_private_group(group_name text)
returns uuid
language plpgsql
security definer
set search_path = ''
as $$
declare
  new_group_id uuid;
  cleaned_name text := trim(group_name);
begin
  if (select auth.uid()) is null then
    raise exception 'Du musst angemeldet sein.';
  end if;
  if char_length(cleaned_name) < 2 or char_length(cleaned_name) > 40 then
    raise exception 'Der Gruppenname muss 2 bis 40 Zeichen lang sein.';
  end if;
  insert into public.groups (name, owner_id)
  values (cleaned_name, (select auth.uid()))
  returning id into new_group_id;

  insert into public.group_members (group_id, user_id, role)
  values (new_group_id, (select auth.uid()), 'owner');
  perform public.write_group_audit(new_group_id, 'group_created');
  return new_group_id;
end;
$$;

create or replace function public.join_private_group(invitation_code text)
returns uuid
language plpgsql
security definer
set search_path = ''
as $$
declare
  target_group public.groups%rowtype;
begin
  if (select auth.uid()) is null then
    raise exception 'Du musst angemeldet sein.';
  end if;
  select * into target_group
  from public.groups
  where invite_code = upper(trim(invitation_code))
  for update;

  if target_group.id is null then
    raise exception 'Der Einladungs-Code ist ungültig.';
  end if;
  if not target_group.invite_enabled then
    raise exception 'Einladungen für diese Gruppe sind deaktiviert.';
  end if;
  if target_group.invite_expires_at is not null and target_group.invite_expires_at < now() then
    raise exception 'Dieser Einladungs-Code ist abgelaufen.';
  end if;

  if exists (
    select 1 from public.group_members
    where group_id = target_group.id and user_id = (select auth.uid())
  ) then
    raise exception 'Du bist bereits Mitglied dieser Gruppe.';
  end if;

  insert into public.group_join_requests (group_id, user_id, status, created_at, decided_at, decided_by)
  values (target_group.id, (select auth.uid()), 'pending', now(), null, null)
  on conflict (group_id, user_id) do update
  set status = 'pending', created_at = now(), decided_at = null, decided_by = null;
  perform public.write_group_audit(target_group.id, 'join_requested', (select auth.uid()));
  return target_group.id;
end;
$$;

create or replace function public.get_group_invite_code(target_group_id uuid)
returns text
language plpgsql
stable
security definer
set search_path = ''
as $$
declare
  result text;
begin
  select groups.invite_code into result
  from public.groups
  join public.group_members on group_members.group_id = groups.id
  where groups.id = target_group_id
    and group_members.user_id = (select auth.uid())
    and group_members.role in ('owner', 'admin');

  if result is null then
    raise exception 'Nur Admins dürfen den Einladungs-Code sehen.';
  end if;
  return result;
end;
$$;


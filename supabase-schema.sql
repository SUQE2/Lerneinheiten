create extension if not exists pgcrypto;

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  display_name text not null check (char_length(display_name) between 1 and 30),
  created_at timestamptz not null default now()
);

create table if not exists public.groups (
  id uuid primary key default gen_random_uuid(),
  name text not null check (char_length(name) between 2 and 40),
  invite_code text not null unique default upper(substr(replace(gen_random_uuid()::text, '-', ''), 1, 8)),
  invite_enabled boolean not null default true,
  invite_expires_at timestamptz,
  owner_id uuid not null references public.profiles(id) on delete cascade,
  max_members smallint not null default 10 check (max_members between 2 and 10),
  created_at timestamptz not null default now()
);

create table if not exists public.group_members (
  group_id uuid not null references public.groups(id) on delete cascade,
  user_id uuid not null references public.profiles(id) on delete cascade,
  role text not null default 'member' check (role in ('owner', 'admin', 'member')),
  joined_at timestamptz not null default now(),
  primary key (group_id, user_id)
);

create table if not exists public.entries (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references public.profiles(id) on delete cascade,
  group_id uuid references public.groups(id) on delete set null,
  entry_date date not null,
  category text not null check (category in ('studium', 'arbeit', 'persoenlich', 'sonstiges')),
  minutes integer not null check (minutes between 1 and 1439),
  topic text not null default '' check (char_length(topic) <= 80),
  visibility text not null default 'private' check (visibility in ('group', 'admins', 'private')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.group_join_requests (
  group_id uuid not null references public.groups(id) on delete cascade,
  user_id uuid not null references public.profiles(id) on delete cascade,
  status text not null default 'pending' check (status in ('pending', 'approved', 'rejected')),
  created_at timestamptz not null default now(),
  decided_at timestamptz,
  decided_by uuid references public.profiles(id) on delete set null,
  primary key (group_id, user_id)
);

create table if not exists public.learning_goals (
  user_id uuid primary key references public.profiles(id) on delete cascade,
  weekly_minutes integer not null default 600 check (weekly_minutes between 30 and 10080),
  updated_at timestamptz not null default now()
);

create table if not exists public.group_audit_log (
  id bigint generated by default as identity primary key,
  group_id uuid not null references public.groups(id) on delete cascade,
  actor_id uuid references public.profiles(id) on delete set null,
  action text not null,
  target_user_id uuid references public.profiles(id) on delete set null,
  details jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create index if not exists entries_user_date_idx on public.entries(user_id, entry_date desc);
create index if not exists group_members_group_idx on public.group_members(group_id);
create index if not exists group_join_requests_group_idx on public.group_join_requests(group_id, status, created_at desc);
create index if not exists group_audit_log_group_idx on public.group_audit_log(group_id, created_at desc);


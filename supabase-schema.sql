$$;

create or replace function public.can_view_join_requester(other_user_id uuid)
returns boolean
language sql
stable
security definer
set search_path = ''
as $$
  select exists (
    select 1
    from public.group_join_requests request
    join public.group_members administrator on administrator.group_id = request.group_id
    where request.user_id = other_user_id
      and request.status = 'pending'
      and administrator.user_id = (select auth.uid())
      and administrator.role in ('owner', 'admin')
  );
$$;

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  insert into public.profiles (id, display_name)
  values (
    new.id,
    coalesce(nullif(trim(new.raw_user_meta_data ->> 'display_name'), ''), split_part(new.email, '@', 1))
  )
  on conflict (id) do nothing;
  return new;
end;
$$;

